package Exer1;

public class DoubleLongFloat {
    public static void main(String[] args) {

        //double, long och float är bara int fast med andra namn.

        int a = 9;
        long b = 9999999999999999L; // måste vara L på slutet... java...
        float c = 2.5F; //måste vara F på slutet... java...
        double d = 5342453252352356322366235623623.4; // som float fast mer siffror och behöver inte f på slutet

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
    }
}
